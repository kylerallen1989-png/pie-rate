const fs = require('fs')

fs.mkdirSync('api', { recursive: true })

const lines = []
lines.push("import type { VercelRequest, VercelResponse } from '@vercel/node'")
lines.push("")
lines.push("const GITHUB_TOKEN = process.env.GITHUB_TOKEN")
lines.push("const CLAUDE_SECRET = process.env.CLAUDE_SECRET")
lines.push("const REPO = 'kylerallen1989-png/pie-rate'")
lines.push("")
lines.push("export default async function handler(req: VercelRequest, res: VercelResponse) {")
lines.push("  if (req.method !== 'POST') return res.status(405).json({ error: 'Method not allowed' })")
lines.push("  if (req.headers['x-claude-secret'] !== CLAUDE_SECRET) return res.status(401).json({ error: 'Unauthorized' })")
lines.push("")
lines.push("  const { path, content, message } = req.body")
lines.push("  if (!path || !content || !message) return res.status(400).json({ error: 'Missing path, content, or message' })")
lines.push("")
lines.push("  try {")
lines.push("    const getRes = await fetch(`https://api.github.com/repos/${REPO}/contents/${path}`, {")
lines.push("      headers: { Authorization: `Bearer ${GITHUB_TOKEN}`, Accept: 'application/vnd.github+json' }")
lines.push("    })")
lines.push("    let sha = undefined")
lines.push("    if (getRes.ok) {")
lines.push("      const existing = await getRes.json()")
lines.push("      sha = existing.sha")
lines.push("    }")
lines.push("    const body: any = {")
lines.push("      message,")
lines.push("      content: Buffer.from(content).toString('base64'),")
lines.push("    }")
lines.push("    if (sha) body.sha = sha")
lines.push("    const putRes = await fetch(`https://api.github.com/repos/${REPO}/contents/${path}`, {")
lines.push("      method: 'PUT',")
lines.push("      headers: { Authorization: `Bearer ${GITHUB_TOKEN}`, Accept: 'application/vnd.github+json', 'Content-Type': 'application/json' },")
lines.push("      body: JSON.stringify(body)")
lines.push("    })")
lines.push("    if (!putRes.ok) {")
lines.push("      const err = await putRes.json()")
lines.push("      return res.status(500).json({ error: err })")
lines.push("    }")
lines.push("    return res.status(200).json({ success: true, path })")
lines.push("  } catch (e: any) {")
lines.push("    return res.status(500).json({ error: e.message })")
lines.push("  }")
lines.push("}")

fs.writeFileSync('api/push.ts', lines.join('\n'), 'utf8')
console.log('wrote api/push.ts')