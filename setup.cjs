const fs = require('fs')

const lines = []
lines.push("import { createContext, useContext, useState, useEffect } from 'react'")
lines.push("import type { ReactNode } from 'react'")
lines.push("import { supabase } from '../lib/supabase'")
lines.push("")
lines.push("interface User {")
lines.push("  id: string")
lines.push("  name: string")
lines.push("  email: string")
lines.push("  role: 'worker' | 'manager' | 'admin' | 'store'")
lines.push("  storeId?: string")
lines.push("}")
lines.push("")
lines.push("interface AuthContextType {")
lines.push("  user: User | null")
lines.push("  loading: boolean")
lines.push("  login: (emailOrStore: string, password: string) => Promise<boolean>")
lines.push("  logout: () => void")
lines.push("}")
lines.push("")
lines.push("const AuthContext = createContext<AuthContextType | null>(null)")
lines.push("")
lines.push("const STORE_PASSWORDS: Record<string, string> = {")
lines.push("  '957': 'waukegan1', '1183': 'grayslake1', '1963': 'mchenry1',")
lines.push("  '3175': 'dekalb1', '3309': 'elgin1', '3407': 'shorewood1',")
lines.push("  '3999': 'lincolnpark1', '4037': 'burbank1', '4106': 'southloop1',")
lines.push("  '4109': 'rivernorth1', '4870': 'ottawa1', '4929': 'yorkville1',")
lines.push("  '5070': 'aurora1', '5143': 'bolingbrook1',")
lines.push("}")
lines.push("")
lines.push("async function fetchProfile(userId: string): Promise<User | null> {")
lines.push("  const { data, error } = await supabase.from('profiles').select('*').eq('id', userId).single()")
lines.push("  if (error || !data) { console.error('Profile fetch error:', error); return null }")
lines.push("  return { id: data.id, name: data.name, email: data.email, role: data.role, storeId: data.store_id }")
lines.push("}")
lines.push("")
lines.push("export function AuthProvider({ children }: { children: ReactNode }) {")
lines.push("  const [user, setUser] = useState<User | null>(null)")
lines.push("  const [loading, setLoading] = useState(true)")
lines.push("")
lines.push("  useEffect(() => {")
lines.push("    const stored = sessionStorage.getItem('pie_rate_store')")
lines.push("    if (stored) {")
lines.push("      setUser(JSON.parse(stored))")
lines.push("      setLoading(false)")
lines.push("      return")
lines.push("    }")
lines.push("    let mounted = true")
lines.push("    supabase.auth.getSession().then(async ({ data: { session } }) => {")
lines.push("      if (session?.user && mounted) {")
lines.push("        const profile = await fetchProfile(session.user.id)")
lines.push("        if (mounted) setUser(profile)")
lines.push("      }")
lines.push("      if (mounted) setLoading(false)")
lines.push("    })")
lines.push("    return () => { mounted = false }")
lines.push("  }, [])")
lines.push("")
lines.push("  const login = async (emailOrStore: string, password: string): Promise<boolean> => {")
lines.push("    if (STORE_PASSWORDS[emailOrStore] === password) {")
lines.push("      const u: User = { id: emailOrStore, name: 'Store #' + emailOrStore, email: emailOrStore, role: 'store', storeId: emailOrStore }")
lines.push("      setUser(u)")
lines.push("      sessionStorage.setItem('pie_rate_store', JSON.stringify(u))")
lines.push("      return true")
lines.push("    }")
lines.push("    const { data, error } = await supabase.auth.signInWithPassword({ email: emailOrStore, password })")
lines.push("    if (error) { console.error('Login error:', error.message); return false }")
lines.push("    if (data.user) {")
lines.push("      const profile = await fetchProfile(data.user.id)")
lines.push("      if (profile) { setUser(profile); return true }")
lines.push("    }")
lines.push("    return false")
lines.push("  }")
lines.push("")
lines.push("  const logout = async () => {")
lines.push("    sessionStorage.removeItem('pie_rate_store')")
lines.push("    setUser(null)")
lines.push("    await supabase.auth.signOut()")
lines.push("  }")
lines.push("")
lines.push("  return <AuthContext.Provider value={{ user, loading, login, logout }}>{children}</AuthContext.Provider>")
lines.push("}")
lines.push("")
lines.push("export function useAuth() {")
lines.push("  const ctx = useContext(AuthContext)")
lines.push("  if (!ctx) throw new Error('useAuth must be used within AuthProvider')")
lines.push("  return ctx")
lines.push("}")

fs.writeFileSync('src/context/AuthContext.tsx', lines.join('\n'), 'utf8')
console.log('wrote AuthContext.tsx')